// String
let string_demo = "This is a string";
console.log('String: ', string_demo, ' -> ', typeof string_demo);

// Number
let number_demo = 25;
console.log('Number: ', number_demo, ' -> ', typeof number_demo);

// Float
let float_demo = 25.5;
console.log('Float: ', float_demo, ' -> ', typeof float_demo);

// Boolean
let bool_demo = true;
console.log('Boolean: ', bool_demo, ' -> ', typeof bool_demo);

// Null
let null_demo = null;
console.log('Null: ', null_demo);

// Undefined
let undefined_demo = undefined;
console.log('Undefined with value: ', undefined_demo, ' -> ', typeof undefined_demo);
let undefined_demo2;
console.log('Undefined without value: ', undefined_demo2, ' -> ', typeof undefined_demo2);

// Objects
let object_demo = {
    width: 250, 
    height: 250, 
    color:'red'
};
console.log('Objects: ', object_demo, ' -> ', typeof object_demo);

// Arrays
let array_demo = ['Apple', 'Mango', 'Orange'];
console.log('Array: ', array_demo, ' -> ', typeof array_demo);